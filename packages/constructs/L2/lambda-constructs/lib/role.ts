import { MdaaConstructProps } from '@aws-mdaa/construct'; //NOSONAR
import { MdaaRole, MdaaRoleProps, IMdaaRole } from '@aws-mdaa/iam-constructs';
import { Duration, Stack } from 'aws-cdk-lib';
import { IManagedPolicy, PolicyDocument, PolicyStatement, ServicePrincipal } from 'aws-cdk-lib/aws-iam';
import { MdaaNagSuppressions } from '@aws-mdaa/construct'; //NOSONAR
import { Construct } from 'constructs';

export interface MdaaLambdaRoleProps extends MdaaConstructProps {
  readonly logGroupNames?: string[];
  readonly externalIds?: string[];
  readonly managedPolicies?: IManagedPolicy[];
  readonly inlinePolicies?: {
    [name: string]: PolicyDocument;
  };
  /**
   * Q-ENHANCED-PROPERTY
   * Optional IAM path for Lambda role organization and management enabling hierarchical role structure. Specifies the IAM path for the Lambda execution role providing organizational structure and access control grouping.
   *
   * Use cases: Role organization; Hierarchical structure; Access control grouping; IAM management
   *
   * AWS: AWS IAM role path for Lambda execution role organization and management
   *
   * Validation: Must be valid IAM path string if provided; defaults to root path '/' for role organization
   **/
  readonly path?: string;
  readonly permissionsBoundary?: IManagedPolicy;
  readonly roleName: string;
  readonly maxSessionDuration?: Duration;
  /**
   * Q-ENHANCED-PROPERTY
   * Optional description for the Lambda execution role enabling role documentation and management clarity. Provides human-readable description of the Lambda role's purpose and permissions for operational understanding and management.
   *
   * Use cases: Role documentation; Management clarity; Operational understanding; Role purpose explanation
   *
   * AWS: AWS IAM role description for Lambda execution role documentation
   *
   * Validation: Must be descriptive text up to 1000 characters if provided; recommended for role documentation
   **/
  readonly description?: string;
}

export type IMdaaLambdaRole = IMdaaRole;

export class MdaaLambdaRole extends MdaaRole {
  private static setLambdaProps(props: MdaaLambdaRoleProps): MdaaRoleProps {
    const overrideProps = {
      assumedBy: new ServicePrincipal('lambda.amazonaws.com'),
    };
    return { ...props, ...overrideProps };
  }

  constructor(scope: Construct, id: string, props: MdaaLambdaRoleProps) {
    super(scope, id, MdaaLambdaRole.setLambdaProps(props));

    if (props.logGroupNames) {
      this.addLogGroups(props.logGroupNames);
    }
  }
  private addLogGroups(logGroupNames: string[]) {
    const logGroupArns = logGroupNames.map(
      logGroup => `arn:${Stack.of(this).partition}:logs:*:*:log-group:/aws/lambda/${logGroup}*`,
    );
    const logStreamStatement = new PolicyStatement({
      resources: logGroupArns,
      actions: ['logs:PutLogEvents', 'logs:CreateLogStream'],
    });
    this.addToPolicy(logStreamStatement);
    const logGroupStatement = new PolicyStatement({
      resources: logGroupArns,
      actions: ['logs:CreateLogGroup'],
    });
    this.addToPolicy(logGroupStatement);
    MdaaNagSuppressions.addCodeResourceSuppressions(
      this,
      [
        {
          id: 'AwsSolutions-IAM5',
          reason: 'LogStream names dynamically generated by Lambda. Wildcard limited to log stream name.',
        },
        {
          id: 'NIST.800.53.R5-IAMNoInlinePolicy',
          reason: 'Inline policy is specific to this role and its custom resource.',
        },
        {
          id: 'HIPAA.Security-IAMNoInlinePolicy',
          reason: 'Inline policy is specific to this role and its custom resource.',
        },
        {
          id: 'PCI.DSS.321-IAMNoInlinePolicy',
          reason: 'Inline policy is specific to this role and its custom resource.',
        },
      ],
      true,
    );
  }
}
